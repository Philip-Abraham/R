## Common transforms

abs(-2) #absolute value
sqrt(3.75) #square root
ceiling(3.475) #is 4
floor(3.475) #is 3
round(3.475,digits=2) #is 3.48
signif(3.475,digits=2) #is 3.5
log(5) #natural logarithm
log2(5) ; log10(5) #other common logs
exp(5) #exponentiating 5
sin(30) # sin 30 rads
cos(30) # cos 30 rads
